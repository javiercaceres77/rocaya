Continuamos por la tarde probando el resto de vías del sector. Personalmente me gustaron mucho ya que tienen algo diferente, especialmente en el comienzo más tipo bulder con pasos de bloque al comenzar para una vez pasada la difultad continuar con la clásica adherencia pedricera. "Matanza en el Burguer King" (V+) bonitos pasos que superan un techito, ojo que la cosa está algo pulida y el grado engaña, "Anónima" (V+), "Mostradores López" (6a+)...
Un día muy interesante que nos dejó con muy buen sabor de boca.