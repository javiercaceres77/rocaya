Domingo 15 de Mayo a las 7:30 de la mañana: Suena el despertador...
<br />
7:40 de la mañana, suena el despertador de nuevo...<br />
No puedo levantarme a estas horas un domingo, escalar en Madrid se está convirtiendo en un infierno para los que tenemos un cierto aprecio por el sueño. Por fin tras un rato haciendome el remolón para no levantarme consigo abandonar la cama y empezar a prepararme para cruzar la barrera de La Pedri antes de que la chapen como es habitual los fines de semana al completarse el aforo.<br />
Cada minuto que pasaba me iba arrepintiendo más y más pero en cuanto me calcé los gatos la cosa empezó a cambiar convirtiéndose finalmente en un buenísimo día de escalada apurando hasta la puesta de sol...
No hace falta dar muchos datos del Cancho de los Brezos porque ¿quién no sabe de donde está? Pero bueno para seguir los formalismos lo pondremos
