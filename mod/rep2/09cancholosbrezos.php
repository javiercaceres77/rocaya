Comenzamos con la primera del día, "Niña Incordio" en el sector principal. Una vía bonita, que comienza en una veta para salir de ella a mitad de vía. Es bastante expuesta, no te caigas de la segunda chapa o te partirás las piernas y el paso de la última a la reu me cuesta ya que el aleje es de más de 5 m sin que afloje el grado salvo en el último paso. ¡Así es la Pedriza! Es lo que decimos para autoconvencernos ¿no? Cuando veo a otras cordadas hacerla, los juramentos se repiten...
