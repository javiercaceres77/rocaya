¿Alguna vez has pensado en escalar en sitios exóticos? ¿Te apasiona la pedriza? Pues bienvenido a Hampi. Situado en lo más recóndito de la India central, 
este bonito pueblo con aires a la pedriza, es meca de boulderistas de todas las nacionalidades.<br />
Hampi está situado en la región de Karnakata State. Si venís en Tren o en Autobús, llegareis a Hospet, a unos 10 km, 
habréis de coger un ricksaw, ya sabéis, esas especies de motos de 3 ruedas totalmente inseguras. Suelen cobrar 25 rps (Abril 2011) 
por persona para llevarte al pueblo y el doble para salir, la India es así, los turistas somos rupias andantes.
