Hampi está dividido por un rio. A un lado Hampi market, el cual tiene también los templos (Recomendable alquilar una bici y recorrerlos, la mayoría son gratis), al otro 
lado el Hampi mas hippie y bonito con los arrozales y numerosas guesthouses donde podrás alojarte.
