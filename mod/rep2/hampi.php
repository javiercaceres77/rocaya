Llegar a los boulders es cosa fácil y se puede acceder andando unos 15 minutos, pero es recomendable madrugar o el sol casi ecuatorial os abrasará las ideas. Hay vías a la sombra pero aun así el calor de medio día es infernal.<br />
La temporada de escalada va desde octubre a febrero que es cuando el calor menos aprieta, de marzo a junio es verano en la india y después hay varios meses de monzón.<br />
Si estabais de paso por la India y no pensabais escalar, no hay problema, existen varios sitios que lo alquilan todo. Nosotros, después de regatear mucho, cogimos un crash pad y 2 pares de pies de gato por 200 Rps, un día. Ahora, eso sí, no esperes material de última generación, el crash pad daba pena y los pies de gato tenían agujeros, pero el abuelillo que nos lo alquilo no daba para más.<br />
Por fin estamos ya en nuestra primera roca, no es nada del otro mundo, solo dos pasos cortos y fáciles, pero íbamos con gente novata y no conocíamos la zona bien. Existe una guía en caso de que quieras adentrarte más, en la cual hay vías fáciles como de grado alto, vamos igual que en la pedri. La guía la puedes encontrar en el pueblo, pero prepárate a pagarla, no es barata.
