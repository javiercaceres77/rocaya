Todos alguna vez hemos pensado en montarnos nuestro propio roco casero para no tener que ir al del gimnasio y así ahorrarnos unas pelillas. Pues aquí os ponemos una guía para poder 
montártelo en tu casa. Sólo necesitas tiempo, paciencia, algo de pasta y sobre todo muchas ganas de currártelo. Vamos  a comenzar el primer capítulo de la serie por las presas de resina, 
como hacerlas, materiales, precios, etc. para que no te queden dudas.<br>
Antes de nada debo decir que este artículo se basa en mi propia experiencia a base de prueba y error por lo que esto no va a misa, a mi me ha funcionado lo cual no quiere decir 
que al resto no le funcione de otra forma. Pues al lío.<br>
Hay varias formas de fabricar presas, tenemos que tener claro si son para colocar en un muro de ladrillo/mortero o similar o en un panel de DM/aglomerado. Para el primero yo 
prefiero optar por presas naturales &ldquo;pegadas&rdquo; con Sikadur 31, una resina epoxi a prueba de bombas que va realmente bien. Nuestro caso en cuestión, son las presas para plafón 
que realizaremos con Resina de Poliéster.<br>
Lo primero hay que irse a una tienda de composites (materiales compuestos), hay muchas, algunas mandan por internet pero  lo mejor es que os acerquéis y os dejéis aconsejar. 
Los materiales que tenemos que comprar serán los siguientes:<br>
Materiales: Resina de poliéster + acelerante + catalizador, Silicona para poliéster+agente de curado, arena fina, espuma verde de macetas, arandelas,  báscula, jeringa (para dosificar)<br>
Comenzaremos por realizar nuestro molde. En un principio hicimos positivos de arcilla y les dimos la textura granulada con un cepillo de púas metálicas y luego uno de dientes. 
El resultado no fue malo del todo pero se consiguen mucho mejores resultados con un material barato y sencillo de moldear. Se trata de la espuma verde que se coloca en las macetas, 
creo que se trata de una espuma fenólica de célula abierta con gran capacidad para absorber el agua. Utilizaremos este material ya que sus poros nos darán la textura deseada a la 
presa ya que si no quedaría muy pulida y resbalaría. Aquí la imaginación es nuestro aliado y trataremos de realizar la forma deseada cortando las piezas con un cuchillo, cuter, las 
manos, etc. No dejéis aristas vivas salvo en la base ya que si no, no será muy agradable al tacto.<br><br>
