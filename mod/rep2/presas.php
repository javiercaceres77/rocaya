Ya lo probé en su momento y el resultado es bastante malo, no se cubre bien y la textura es mala, por lo que tendréis que limar la pieza y eso es una chapu.<br>
La silicona guapa es cara, por lo que hay que tener muy claro lo que vas a hacer. Nosotros empleamos una llamada Silastic 3481 con su agente de curado correspondiente. Son siliconas 
líquidas que una vez añadido el catalizador, endurecen con el paso de un tiempo. Por tanto, tendréis que hacer un recipiente donde verter la silicona y alojar vuestro positivo 
(la pieza de espuma verde) sin que toque el fondo del primer recipiente. Pensad que cuanto más economicéis mas moldes podréis sacar. Es algo complicado encontrar una forma de no 
gastar mucha silicona pero al final lo encontrareis. El bote de un kilo viene saliendo por unos 25 € y a mí me dio para hacer 3 o 4 piezas medias/grandes.<br><br>
