Cobran 3 € por entrar aunque yo siempre que la he hecho al ser fuera de temporada no hay nadie. Se cruza el río y comienzas en unos bloques de piedra con las primeras grapas. Ojo nada más cruzar el río me pondría el casco ya que nos cayeron dos pedazo de piedras que si nos pillan la avería es importante...
El entorno es espectacular con una cuenca en la que se leen perfectamente los pliegues geológicos del terrano y te puedes hacer una idea de lo pequeños que somos en este planeta. La primera parte es la más farragosa, al menos el día que la hicimos, había llovido a muerte hasta 5 minutos antes y estaba todo empapado y embarrado con el consiguiente peligro de resbalar.
