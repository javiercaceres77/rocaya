¿Quién no ha oído hablar del Yelmo alguna vez en la vida? Si  eres escalador estarás cansado de oír una y otra vez el nombre de esta  impresionante mole de granito. Si no lo eres pero te gusta la naturaleza,  alguna vez habrás visto su característica forma cuando observas La Pedriza  desde lejos.<br>
Fue el primer risco escalado del que tenemos noticias (por supuesto  que posiblemente antes los pastores de la zona habrían llegado a la cima) y  ello ocurría en el año 1864. Desde entonces se han abierto muchas vías y es  lugar de peregrinaje habitual para escaladores y senderistas.<br>
