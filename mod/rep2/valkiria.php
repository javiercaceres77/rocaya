Hay muchas formas de subir al risco Peña del Diezmo, la más  asequible, la vía normal por la cara norte, consiste en una simple trepada muy  bonita con un final algo encajonado que hay que pasar como en chimenea.  La segunda forma, también sin necesidad de  cuerdas, es la vía Valentina. Éste es el itinerario que se utiliza  habitualmente para descender de El Yelmo sin necesidad de rapelar o hacerlo por  la vía normal en la cara norte. Ojo con la Valentina que a pesar de su  facilidad tiene 3 pasos complicados, al principio y en la mitad que pueden  comprometer la ascensión o el descenso, yo sin pies de gato no lo haría…<br>
Por último la otra forma asequible de subir a la cima es la  famosa y repetidísima vía Walkiria, una vía fácil y disfrutona, con un pasos de  adherencia y una buenísima fisura para proteger. Esta es sin duda la mejor  forma de empezar a escalar las números vías que recorren este risco.<br>
