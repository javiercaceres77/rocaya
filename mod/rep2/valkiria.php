Hay una repisa enorme y cómoda para poder empezar. De esta  forma te ahorras unos metros de exposición hasta la primera chapa. Nosotros  hasta que pudimos chapar la primera pudieron pasar unos 20 metros que si bien  son fáciles mejor que no te caigas. <br>
