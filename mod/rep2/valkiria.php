El largo será IV+ con dos o tres pasos de V que con el roce  de la cuerda (especialmente si vas con cuerdas dobles pueden convertirse en V+  y pasar alguna penuria en días de calor. Estos pasos están protegidos con  alguna chapa por lo que no vas a encontrar demasiados problemas teniendo la  posibilidad de escaquearte algo por la derecha si no lo ves claro. Llegamos a  la reunión con 3 parabolts muy bien montada.<br>
