<h4>Descripción de la vía:</h4>
<h5>Primer largo:</h5>
Nosotros comenzamos desde abajo del todo pero la salida es  complicada y te metes debajo de un árbol que molesta bastante por lo que es  recomendable empezar arriba a la izquierda bordeando el gendarme. <br>
