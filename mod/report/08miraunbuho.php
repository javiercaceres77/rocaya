Un mini repor de unas escaladas que hicimos en Febrero y tenía pendiente subir. Concretamente la vía "Mira un Búho" del Risco de los Principiantes, que de principiantes no tiene precisamente demasiado...aunque según dicen se utilizaba antaño para enseñar a los novatos. Eso eran montañeros y no lo que somos ahora juas!<br />
Señalo en cuestión las dos clásicas  más repetidas del risco y que sin duda son dos de las que tienes que marcar o haber hecho alguna vez. Se puede comenzar con el "Diedro" aunque remarcar que es una bavaresa bastante física así que calentad los brazos y llevad los friends bien ordenados para no cagarla en mitad de subida. El "Diedro" es V y se hace en un sólo largo. En ninguna de las dos vías hay nada salvo las reus.<br /> Las fotos que aquí pongo son de "Mira un Búho" dos largos V y V+ fáciles pero para ir con el grado consagrado ya que una galleta en el segundo largo y seguro que des contra la pared de enfrente al ser medio travesía la fisura del segundo largo. Es una pena que no echáramos más fotos especialmente de la salida del primer largo que es precioso pero había otra cordada esperando y no era plan de eternizarse. Decir que estas vías suelen ser bastante repetidas así que ojito si vais en finde...

