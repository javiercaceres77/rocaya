Para quien no haya estado comentar simplemente que es una bonita zona a la que se accede desde el puerto de Cotos en algo más de media hora de pateo. Hay un montón de sectores algunos equipados y otros no que bien merecen una visita. En este caso nos centramos en la pared del "Contrafuerte de la Teresa".<br />
