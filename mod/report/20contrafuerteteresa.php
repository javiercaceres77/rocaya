Concretamente el Contrafuerte de la Teresa tiene una orientación sureste si bien el resto de los sectores varían entre este y nordeste algunos de ellos. Por tanto el sol está casi garantizado por la mañana y en el caso de nuestro muro en septiembre hasta bien entrada las 5 de la tarde. Así pues, un día de esos de calor calor que no corra una gota de aire no es buena idea venir a esta zona a pesar de la altura. Otra cosa es que corra una pequeña brisa que a estas alturas (2000 m ?) la cosa refresca bastante.<br />
