Nos alojamos en el camping y hay que pillar el coche unos 10 minutos hasta un parking. De aquí un pateo cuesta arriba especialmente una vez te sales del camino de una hora aprox. dependiendo de la vía.<br />
La vía son 5 largos de una dificultad máxima de 6a. El primero con un pequeño paso de V o V+ para ir cogiendo coco. La R1 supercómoda. Segundo largo lo más difícil de la ruta con una especie de bavaresa en la que hay que buscar apoyos finos para manos y pies, 6a un metro o 2 y el largo cortito hasta la R2 bastante cómoda también. Largo 3 muy asequible por un espolón hiperchapado hasta la R3 bastante incómoda. Largo 4 más o menos de V hasta la R5 donde tras unos pasitos delicados y aéreos suaviza mucho para llegar a un último techito de 3 metros protegido con dos chapas que le pone la guinda a la vía.<br />
