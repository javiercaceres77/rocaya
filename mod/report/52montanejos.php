La zona se encuentra encajonada a los pies del río y en esta época (invierno) le da la sombra por lo que el frescor está garantizado.<br />
Nada más pasar el túnel (no aparcar en los miradores) hay un apartadero donde se puede aparcar y bajar por una empinada senda hasta los márgenes del río. Previamente y a la altura de una cata en la roca (supongo que para la presa) hay que bajar por unos peldaños metálicos con ayuda de una cuerda. <br />
