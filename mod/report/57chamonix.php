Se trata de uno de los lugares que todo montañero debería conocer alguna vez en la vida y es que al oir hablar de Chamonix en nuestras mentes surgen una sucesión de imágenes de escalada, alpinismo y alta montaña que en algún momento hemos visto, oído o experimentado. Ésta es una de las mecas de escalada que harán las delicias de los más intrépidos que se aventuren en sus verticales fisuras de granito rojo.<br />
