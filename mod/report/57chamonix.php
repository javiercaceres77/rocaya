Caminar por las calles de Chamonix, observar la grandiosidad del Mer de Glace, perder la vista en las afiladas agujas que se levantan imponentes en lo más alto de las montañas simplemente no tiene precio.<br />
Una vez coges el teleférico que te sube al Aiguille du Midi (en dos etapas) llegas a los 3800 metros donde puedes observar cruzando una pasarela la verticalidad de las paredes a las que te vas a tener que enfrentar.<br />
Hay posibilidad de coger otro teleférico que por encima del glaciar del Gigante te lleva hasta la punta Hellbroner ya en Italia.<br />
