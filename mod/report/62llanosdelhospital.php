A mediados de Febrero cambiamos nuestras rutinas para dejar de lado el magnesio y los pies de gato por las skis y los piolets con intención de hacer un algo de hielo y unos días de travesía. Sin embargo los elementos se pusieron en nuestra y aunque parecía que nuestras vacaciones se habían arruinado resulto ser un viaje tremendamente divertido a pesar de no salir como esperabámos.<br />
Primero petó nuestro coche en Huesca, después de perder toda la mañana y parte de la tarde en el taller conseguimos que el seguro nos pusiera un taxi a nuestro destino, Llanos del Hospital. Llegamos de noche y nevando, con plena ventisca, y un nuevo contratiempo puso en jaque nuestra expedición. Esta vez era la carretera que llega a Llanos, permanecía cortada 8 km antes del hotel por peligro de avalanchas. Aquí nos dejó el taxi y cargados como mulas comenzamos a remontar carretera arriba los 8 km parte porteando y parte traveseando.<br />
Nuestra intención era llegar esta noche al refu de la Renclusa pero el porteo y las horas nos pasaron factura y alguna rozadura y tuvimos que hacer noche en el hotel.<br />
