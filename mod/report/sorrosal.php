Una vez llegas arriba no hay que quitarse el arnes ya que los tramos equipados continúan. Abrimos una puerta metálica en el bosque (dejar cerrada que hay ganado..) y después de unos tramos horizontales llegamos a un último resalte vertical que hay ascender. Otro tramo horizontal y se acabó. Salimos a una pradera en medio del bosque y siguiendo la senda PR (marcas blancas y amarillas) comenzamos a descender en 20 minutos hasta Broto.
Una vía ferrata muy recomendable y completa, una buena posibilidad para alternar esos días que no sabes que hacer o como en este caso, que está todo mojado aunque hay que tener en cuenta que si bien es complicado caerse y el equipamiento es muy bueno, hay que estar mucho más atento al estar todo mucho más resbaladizo.