Sábado 30 de Abril de 2011. Estamos en Jaca y como no en nuestra línea nos levantamos a las 12 con la intención de ir a trepar algo. Siguiendo nuestro frenético ritmo de actividades, comemos con toda la calma en la terraza y cuando nos damos cuenta son las 4 30... Esto es aprovechar el día, dice alguien por ahí. En el último momento decidimos cambiar de rumbo y dirigirnos a Broto para hacer una ferrata ya que hay unas nubes que amenazan lluvia y la roca del día anterior debe estar bastante mojada. Y acertamos, puesto que nada más salir de casa un tormentón lo llena todo de agua y nos tendríamos que haber dado la vuelta.
La vía ferrata en cuestión es La Cascada del Sorrosal, un espectacular salto doble de agua del río del mismo nombre a apenas 5 minutos del pueblo de Broto.
